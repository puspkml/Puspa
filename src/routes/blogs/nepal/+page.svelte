      <h2>Protests, Bullets, and a Bloody Silence</h2>
      <p>When the government thought they had silenced dissent by banning social media, the streets of Kathmandu roared louder.</p>

    
      <p><strong>Tens of thousands</strong> marched toward the <strong>Federal Parliament building in New Baneshwar</strong>, chanting:</p>
      <blockquote>
        <strong>“End Corruption!”</strong><br/>
        <strong>“Down with Oli!”</strong>
      </blockquote>


