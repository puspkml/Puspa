      <h2>The Spark That Ignited the Inferno</h2>

      <p>The government’s decision to <strong>ban 26 social media platforms</strong>, including Facebook, X, and YouTube, citing “failure to register under state rules”, was the last straw for a generation already fed up with systemic rot.</p>



      <section class="max-w-5xl mx-auto px-6 py-10">
        <div class="grid grid-cols-1 md:grid-cols-2 gap-6 items-start">
          

          <!-- Text -->
          <div class="prose prose-slate max-w-none text-justify">
           <p>But here’s the truth people whispered among peoples, and yelled in the streets:</p>

            <blockquote>
              <em>This was <strong>never about registration</strong>.<br/>This was <strong>about corruption</strong>.</em>
            </blockquote>

            <p>For months, social media had been the <strong>people’s courtroom</strong>. Youth-led exposés revealed the lavish lifestyles of politicians’ children—draped in designer brands, flaunting luxury cars, sipping champagne in elite clubs. All this, while <strong>ordinary Nepalis struggle with unemployment and rising inflation</strong>. The posts went viral. The hashtags trended. And the government panicked.</p>

            <p>So they pulled the plug.</p>

