      <p>
        Alright guys, so recently we had <strong>Ganesh Chaturthi</strong> — and let me tell you, this time I didn't just show up for the sweets. Nope. Somehow, I found myself getting promoted from “guy-who-just-lifts-chairs” to “all-in-one workshop technician.”
      </p>

      <p>
        One second I was made to hammer and bend the metallic rods, another second I was asked to cut Styrofoam into desired shapes and before I knew it, I was helping weld chariot parts with <em>Annas</em>. And if that wasn't enough, I even ended up in the arts-and-crafts department — sticking paper-mâché like a preschool kid but with way more responsibility. Honestly, if LinkedIn had a category for “Chariot-Making Intern,” I'd be endorsing myself right now.
      </p>

    
      <p>
        And while we were working overnight, here's a thought that hit me:  
        <em>"When our acts are oriented towards God, we unlock this extra gear in us".</em> Suddenly, staying awake all night, carrying heavy stuff, bending metal rods — it doesn't feel like suffering, it feels purposeful.
      </p>

      <h2>Science, God, and the Big Question</h2>
      <p>
        Now, confession time: I've always had this long-standing question about the <strong>existence of God</strong>. Growing up, I heard the usual things from parents and elders — “God is divine, God is angelic.” But the physics student in me was like, “Okay, cool… but can we please define this in terms of equations and constants?”
      </p>

      <p>
        Stephen Hawking in his book <em>Later Interviews & Writings</em> said, <q>Before we understood science, it was natural to believe that God created the universe. But now science offers a more convincing explanation.</q>  
        </p><p>
        But here's my problem — science never completely convinced me either. Because as a kid, I saw things science couldn't explain. Like vibhooti (sacred ash) literally appearing out of nowhere, honey dripping from photo frames. And trust me, when you see that with your own eyes, you don't just shrug it off.
      </p>

      <h2>The Physics Classroom</h2>
      <p>
        I still remember my first physics class in my pre-collage (class 11). The teacher came in, wrote in big, bold letters:  
        <strong>“FORCES.”</strong>  
        And he said: <q>There are four fundamental forces in nature. Everything that happens falls under these.</q>
      </p>

      <p>
        And me, the ever-curious troublemaker, raised my hand:  
        <q>Sir, in which force does the materialization of God — like vibhooti or honey appearing — fit?</q>
      </p>

      <p>
        The whole class froze. And the teacher, clearly not ready for this level of chaos on Day 1, just smiled and said: <q>Those are probably tricks.</q>
      </p>

      <p>
        But here's the thing — I wasn't convinced. Because once you've <strong>seen</strong> something, it doesn't just vanish because it doesn't fit into the textbook. Sure, our eyes can deceive us. But does that mean <strong>everything unexplainable is fake?</strong>
      </p>

      <h2>The feeling of Déjà vu</h2>
      <p>
        Fast-forward to my <strong>college days</strong>. By then, these questions were running in the background of my mind like a never-ending soundtrack. I wasn't an atheist — I prayed, I believed in God — but at the same time, the physicist in me wanted logical closure.
      </p>

      <p>
        The time I was enrolled in M.Sc.(Physics), first week, first lecture. My professor walks in and guess what he writes in bold letters?  
        <strong>“FORCES.”</strong>  
        Déjà vu, right? It felt like I was back in 11th grade. But this time, I didn't ask the question. I thought, “Okay, what's the point? No one's gonna answer it anyway.”
      </p>

      <p>
        But here's where the game changed. The professor himself asked:  
        <q>Among all these forces, where does spirituality fall? Have you ever seen materialization — things being created? Which of these forces does that come under?</q>
      </p>

      <p>
        And then he said something I'll never forget:  
        <em>“There is something beyond our knowledge. A science we still don't understand. That science is spirituality.”</em>
      </p>

      <h2>The Full Circle</h2>
      <p>
        Bhagawan Sri Sathya Sai Baba on 6th May 1988 (Ofcourse I was not born... I read it in Sri Sathya Sai Speaks, Volume 21), during his discourse (<a data-sveltekit-preload-data="hover" href="https://saispeaks.sathyasai.org/discourse/scientist-and-saint">
        The Scientist and The Saint</a>)
        at Prasanthi Nilayam, said,
        <em>“Science is partial, spirituality is wholistic”</em>.
      </p>

      <p>
        And it clicked. Science talks about atoms, protons, neutrons, quarks — invisible building blocks present everywhere around us. Spirituality talks about Brahman, God, Consciousness — also present everywhere around us.
      </p>

      <p>
        Different languages. Same reality.
      </p>

      <p>
        The only problem? Maybe our human brain just doesn't have the bandwidth to fully grasp it yet. So what do we do? We argue. We doubt. We create chaos — science vs. spirituality, faith vs. reason. And maybe, just maybe, that argument will always be there.
      </p>

      <p>
        Maybe the day we stop looking at them as enemies, and start seeing them as mirrors, we'll finally understand.  
        Or maybe… we'll keep asking forever.
      </p>
